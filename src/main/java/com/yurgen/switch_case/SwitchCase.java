package com.yurgen.switch_case;

public class SwitchCase {

    public static void main(String[] args) {
        int i = 0;
        switch (i) {
            case 0:
                System.out.println("НОЛЬ");
                System.out.println("НОЛЬ");
                System.out.println("НОЛЬ");
                System.out.println("НОЛЬ");
                System.out.println("НОЛЬ");
            break;
            case 1:
                System.out.println("ОДИН");
                break;
            case 2:
                System.out.println("ДВА");
                break;
            default:
                System.out.println("НЕ ЧИСЛО");
        }
    }

}
